--多加内功(三阴玄针)

function buff8044Attri(Char, buff, Attri)

		Attri:SetAtk(Attri:GetAtk() * 1.01);
		Attri:SetDef(Attri:GetDef() * 1.01);
		Attri:SetEva(Attri:GetEva() * 1.01);
		Attri:SetHit(Attri:GetHit() * 1.01);
		Attri:SetPow(Attri:GetPow() * 1.01);
		Attri:SetMaxHp(Attri:GetMaxHp() * 1.1);
		Attri:SetSpd(Attri:GetSpd() * 1.01);
end
